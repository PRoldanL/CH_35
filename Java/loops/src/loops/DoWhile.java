package loops;

public class DoWhile {

	public static void main(String[] args) {
		
		/*
		 
		El ciclo do while evalua la expresion en la parte inferior del ciclo, por lo que las declaraciones dentro del bioque "do" siempre se ejecutara al menos una vez.
		 
		*/
		
		//Cuenta del 1 al 20
		
		int cuenta= 5;
		do {
			System.out.println(cuenta);
			cuenta++;
						
		} while (cuenta < 0); // Output 1 ya se ejecutÃ³ al menos 1 vez		

		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
